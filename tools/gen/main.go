package main

import (
	"bytes"
	"fmt"
	"go/format"
	"log"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"text/template"
)

const modulePath = "log-analyzer"

const pluginsDir = "."

const outputFile = "plugins_gen.go"

const fileTemplate = `// Code generated by tools/gen; DO NOT EDIT.
// Re-run with: go generate ./...
package plugins

import (
{{- range .Imports }}
	_ "{{ . }}"
{{- end }}
)
`

func main() {
	plugins, err := discoverPlugins(pluginsDir)
	if err != nil {
		log.Fatalf("plugin discovery failed: %v", err)
	}

	imports := make([]string, 0, len(plugins))
	for _, name := range plugins {
		imports = append(imports, fmt.Sprintf("%s/plugins/%s", modulePath, name))
	}

	if err := writeFile(filepath.Join(pluginsDir, outputFile), imports); err != nil {
		log.Fatalf("write file failed: %v", err)
	}

	fmt.Printf("plugins_gen.go updated with %d plugin(s): %s\n",
		len(plugins), strings.Join(plugins, ", "))
}

func discoverPlugins(root string) ([]string, error) {
	entries, err := os.ReadDir(root)
	if err != nil {
		return nil, fmt.Errorf("read dir %q: %w", root, err)
	}

	var plugins []string
	for _, entry := range entries {
		if !entry.IsDir() {
			continue
		}
		name := entry.Name()
		if strings.HasPrefix(name, ".") || strings.HasPrefix(name, "_") {
			continue
		}

		hasGo, err := dirHasGoFile(filepath.Join(root, name))
		if err != nil {
			return nil, err
		}
		if !hasGo {
			continue
		}

		plugins = append(plugins, name)
	}

	sort.Strings(plugins)
	return plugins, nil
}

func dirHasGoFile(dir string) (bool, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return false, fmt.Errorf("read dir %q: %w", dir, err)
	}
	for _, entry := range entries {
		if entry.IsDir() {
			continue
		}
		name := entry.Name()
		if strings.HasSuffix(name, ".go") && !strings.HasSuffix(name, "_test.go") {
			return true, nil
		}
	}
	return false, nil
}

func writeFile(path string, imports []string) error {
	tpl, err := template.New("file").Parse(fileTemplate)
	if err != nil {
		return fmt.Errorf("parse template: %w", err)
	}

	var buf bytes.Buffer
	if err := tpl.Execute(&buf, struct{ Imports []string }{Imports: imports}); err != nil {
		return fmt.Errorf("execute template: %w", err)
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("gofmt generated code: %w", err)
	}

	if err := os.WriteFile(path, formatted, 0644); err != nil {
		return fmt.Errorf("write %q: %w", path, err)
	}
	return nil
}
